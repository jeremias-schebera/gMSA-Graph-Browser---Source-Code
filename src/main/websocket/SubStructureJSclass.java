public class SubStructureJSclass {
	private String composedName;
	private String name;
	private String color;
	private String startVertexID;
	private String endVertexID;
	
	
	public SubStructureJSclass(String composedName, String name, String color, String startVertexID, String endVertexID) {
		this.composedName = composedName;
		this.name = name;
		this.color = color;
		this.startVertexID = startVertexID;
		this.endVertexID = endVertexID;
	} 
}
